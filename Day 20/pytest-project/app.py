# Basic functions

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def to_upper(text):
    return text.upper()

def list_length(lst):
    return len(lst)

def is_even(num):
    return num % 2 == 0

def divide(a, b):
    return a / b

def square(x):
    return x * x

